export function getSeedEquipmentBySlug(slug: string): Equipment | undefined {
  return getSeedEquipment().find((eq) => eq.slug === slug);
}

export function getSeedRelatedEquipment(equipmentId: string): Equipment[] {
  const all = getSeedEquipment();
  const target = all.find((eq) => eq.id === equipmentId);
  if (!target) return [];

  const sameCategory = all.filter(
    (eq) => eq.category === target.category && eq.id !== equipmentId
  );

  const shuffled = [...sameCategory].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 3);
}

export function getSeedEquipmentImages(
  equipmentId: string
): { id: string; url: string; alt_text: string; is_primary: boolean }[] {
  const eq = getSeedEquipment().find((e) => e.id === equipmentId);
  if (!eq || !eq.primary_image_url) return [];
  return [
    {
      id: `${equipmentId}-1`,
      url: eq.primary_image_url,
      alt_text: eq.name,
      is_primary: true,
    },
  ];
}
